$(function(){
	$("#menu-principal li").bind({
		mouseleave:function(){
			$(this).find("ul:first").hide();
			$(this).removeClass("active");
		},
		mouseenter: function(){
			$(this).addClass("active");
			$(this).find("ul:first").slideToggle("fast");
		}
	});
}); 

